#Fundamental Data Types
#int and float
print (type(2 + 4))
print (type(2 - 4))
print (type(2 * 4))
print (type(2 / 4))

print (type(10.56))

print (type(20.9+1.1))

print(2**2) #exponent. 2 to the power of 2
print(5//4)

print (5 % 4) #modulo - will print the remainder once after division


#Math functions
print (round(3.11))

print (abs(-20))


#Operator Precedence
print(20 - 3 * 4)
print((20 - 3) ** 4)
# () parentheses first
# ** multiplication next
#+ - then addition/subtraction


#complex Numbers
#stored in memory as bin (binary)
print(bin(5))